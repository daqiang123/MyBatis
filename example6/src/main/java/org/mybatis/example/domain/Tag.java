package org.mybatis.example.domain;

/**
 * 标签POJO类
 * 
 * @author 大强
 *
 */
public class Tag {

	public int id;

	public String name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
